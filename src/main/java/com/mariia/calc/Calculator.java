package com.mariia.calc;

public class Calculator {

    int add(int a, int b) {
        return a + b;
    }

    double multiplication(double c, double d) {
        return c * d;
    }

    double division(double e, double f) {
        return e / f;
    }

    int squaring(int g, int i) {
        return (int) Math.pow(g, i);
    }

}
